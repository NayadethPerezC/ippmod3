    <h3>Manowar en Chile</h3>
    <img src="manowar.jpg" class="rounded" alt="IMFPT" width="300" height="400">
    <p>Después de 13 largos años regresa a Chile una de las bandas más reconocidas del heavy metal: MANOWAR. La agrupación liderada por Joey
        DeMaio y Eric Adams confirmó hace un par de meses su regreso a Sudamérica, lo que finalmente ha ocurrido con su concierto en Chile
        para el martes 26 de septiembre en Santiago, anunciado inicialmente en el Teatro Teletón, pero que debido al éxito de ventas, 
        cambia su locación al Teatro Caupolicán. “Los fanáticos han estado esperando ansiosamente este momento, ¡y nosotros también! ¡Estamos 
        encantados de regresar a Sudamérica y haremos de estos espectáculos la experiencia inolvidable que merecen nuestros fans!”, comentó 
        Joey DeMaio. Actualmente, Manowar se encuentra realizando la gira “CrushingTheEnemiesof Metal”, celebrando sus 40 años de carrera y,
        además, el aniversario de seis de sus álbumes que han marcado a toda una generación: BattleHymns, FightingTheWorld, TheTriumphof Steel,
        Warriors OfTheWorld, GodsofWar y The Lord of Steel. Recientemente, la banda anunció a Michael AngeloBatio como su nuevo guitarrista, 
        quien tiene una larga carrera como solista y varios discos junto a Nitro, Holland, Jim Gillette y C4. 

        Sin duda una oportunidad imperdible, donde los fanáticos podrán disfrutar por primera vez en nuestro país los clásicos de la banda,
        con un show demoledor y aún mucho más potente al recordado concierto del 2010. 

